void main(){
	int a = 1; // true
	int b = 0; // false
	
	printf("a = %d\n", a);
	printf("b = %d\n", b);
	
	// logika AND
	printf("a && b = %d\n", a && b);
	
	// logika OR
	printf("a || b = %d\n", a || b);
	
	// logika NOT
	printf("!a = %d\n", !a);
}